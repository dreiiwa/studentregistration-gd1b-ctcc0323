    /**
     * for user input adding to the "studentrecord" section on the database. 
     * While connecting to the database itself.
     */
    private void baddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_baddActionPerformed
        // TODO add your handling code here:
        String name = tfname.getText();
        String email = tfemail.getText();
        String course = tfcourse.getText();
        String number = tfnumber.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/maharlika_funds_state_university", "root", "");  
            pet = con1.prepareStatement("insert into studentrecord(name,email,course,number)values(?,?,?,?)");
            pet.setString (1, name);
            pet.setString (2, email);
            pet.setString (3, course);
            pet.setString (4, number);
            pet.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Student Registration Added.");
            tb_update();
            
            
            tfname.setText("");
            tfemail.setText("");        
            tfcourse.setText("");
            tfnumber.setText("");
            tfname.requestFocus();    
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(reg.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(reg.class.getName()).log(Level.SEVERE, null, ex);
        }      
    }//GEN-LAST:event_baddActionPerformed

    /**
     * for output deleting when you click a specific column on the table, then
     * clicking the delete button.
     */
    private void bdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdeleteActionPerformed
        
        DefaultTableModel df = (DefaultTableModel)recordtable.getModel();
        int selectedIndex = recordtable.getSelectedRow();
        
        try {     
            int id = Integer.parseInt(df.getValueAt(selectedIndex, 0).toString());
            int dialogue = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete?", "Confirm Deletion", JOptionPane.YES_NO_CANCEL_OPTION);
        
            if (dialogue == JOptionPane.YES_OPTION)    
            {

             Class.forName("com.mysql.cj.jdbc.Driver");
            
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/maharlika_funds_state_university", "root", "");  
            pet = con1.prepareStatement("delete from studentrecord where id =?");

            pet.setInt (1, id);   
            
            pet.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Deleted Successfully.");
            tb_update();
            
            
            tfname.setText("");
            tfemail.setText("");        
            tfcourse.setText("");
            tfnumber.setText("");
            tfname.requestFocus(); 
                      
             } 
        } catch (ClassNotFoundException ex){
            Logger.getLogger(reg.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(reg.class.getName()).log(Level.SEVERE, null, ex);
                  
        }
        
    }//GEN-LAST:event_bdeleteActionPerformed

