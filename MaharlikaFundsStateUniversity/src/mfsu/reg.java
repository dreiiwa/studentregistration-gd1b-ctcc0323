    /**
     * for output printing of the table, when input was added, 
     * edited nor deleted.
     */
    private void tb_update() 
    {
        
         int c;
         
         try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/maharlika_funds_state_university", "root", "");  
            pet = con1.prepareStatement("select * from studentrecord");
            
            ResultSet rs = pet.executeQuery();
            ResultSetMetaData rss = rs.getMetaData();
            c = rss.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)recordtable.getModel();
            df.setRowCount(0);
          
                while(rs.next())
                {
                    Vector v2 = new Vector();
                    
                    for(int a=1; a<=c; a++)
                    {
                        v2.add(rs.getString("id"));
                        v2.add(rs.getString("name"));
                        v2.add(rs.getString("email"));
                        v2.add(rs.getString("course"));
                        v2.add(rs.getString("number"));
                    }
                    df.addRow(v2);
                } 
            
            }        
          catch (ClassNotFoundException ex) {
            Logger.getLogger(reg.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(reg.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    }
 
